package datatypeinteger;

public class DataTypeInteger
{
    public static void main ( String[] args )
    {
        int A = 20;
        int B = 4;

        System.out.println ( A + B );
        System.out.println ( A - B );
        System.out.println ( A * B );
        System.out.println ( A / B );
        System.out.println ( A++ );
        System.out.println ( B-- );
        System.out.println ( ++A + --B );
        System.out.println ( ++A - --B );
    }
}
